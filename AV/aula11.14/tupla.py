#Tuplas - sua principal caracteristica é ser imutavel, seu parametro ()
#Se você colocar como variavel dois valores com uma virgula, o python assimila ela como tupla

tupla = ()
tupla = ('Gus')
print(tupla)
tupla = ('Jesus')  #Valor da tupla não pode ser modificada através do indice, somente se for o valor total
print(tupla)